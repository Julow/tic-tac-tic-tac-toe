(** No: No owner;
 ** P1: Player1;
 ** P2: Player2 *)
type t = No | P1 | P2

let next = function
	| P1			-> P2
	| P2			-> P1
	| No			-> No

let char_of_owner = function
  | No					-> 'N'
  | P1					-> 'O'
  | P2					-> 'X'
